      <h2>Us:</h2>
      <p>
        The Open Jam Aotearoa Team are Chris Wratt (noise maker), Tristan Bunn
        (anchor point clicker) and Birgit Bachler (fax machine jammer). The idea
        to create an event like Open Jam Aotearoa was born one night at the
        artist-run venue Pyramid Club during Chris' workshop on "Boring Video
        Games". Chris and Birgit dreamed of an event that weaves the diversity
        of the local experimental arts, design and music scene together with the
        game community, and we are excited to make this dream come true during
        this weekend in March.
      </p>
    </div>

    <div class="col-2">
      <h2>You:</h2>
      <p>
        Open Jam Aotearoa aims to be an open event for participants from all
        backgrounds and skill levels who are keen to spend a weekend together
        with like-minded creative people to develop games. We are especially
        inviting people who do not consider themselves traditional game
        developers and who can bring new perspectives, stories and skills to the
        jam.
      </p>
      <p>
        Participants do not need to be part of a team to be involved, they can
        attend and contribute their skills to any other project at the jam
        freely.
